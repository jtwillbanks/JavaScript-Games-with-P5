function draw() {
	if(gOver)
		return;
	background(25);
	mazeUpdate();
	displayPlayer();
	updatePlayer();
	document.getElementById('score').innerHTML = "Lives: " + lives + '';
	document.getElementById('hScores').innerHTML = "Level: " + level + '';
}

function displayPlayer() {
	fill(0, 128, 0);
	rect(xPos, yPos, 20, 20);
}

function updatePlayer() {
	if(xPos == 620 && yPos == 640) {
		levelWon();
	}
	if(xPos == 20 && yPos == -20) {
		xPos = 20;
		yPos = 0;
	}
}

function levelWon() {
	console.log("Next Level");
	level++;
	xPos = 20;
	yPos = 0;
	if(level > 3) {
		level = 1;
		gOver = true;
		document.getElementById('game').style.opacity = 0.1;
		document.getElementById('gOver').innerHTML = "You Win!!!";
		document.getElementById('gOverText').innerHTML = "Back to level: 1"
		document.getElementById('gOverEsc').innerHTML = "Click to Continue";
	}
}

